<h1>BCC (Basic Computer Course)</h1>
<h1>Introduction:</h1>
<strong><li>Objective of BCC:</li></strong> <p><p> In step with effort to achieve the objective of achieving computer literacy in an inclusive manner, NIELIT has launched a new programme, “Basic Computer Course (BCC)”. The objective of the course is to impart basic level computer appreciation programme with more emphasis on hands on training. Initially, the course was conceived as to boost the concept of introducing a course on computer fundamentals, especially for students, studying vocational courses at ITIs/ITCs. However, the course has gained popularity amongst many more categories. The BCC was launched in year 2011. The programme can equip a person to use the computers in day-to-day life for professional and personal use. After completing the course the incumbent will be computer literate and will be able to:</p>
<p>Acquire confidence in using computer techniques available to users;
Recognise the basic components of computers and terminology;
Understand data, information and file management;
Create documents using Word processor, Spreadsheet & Presentation Software;
Understand computer networks, Internet;
Browse the internet, content search, email and collaborate with peers;
Use e-Governance applications;
Use computer to improve existing skills and learn new skills.</p>
<strong>Eligibility:</strong> <h4>The candidates can appear in the NIELIT BCC Examination through following modes :</h4>

<li>1 Candidates sponsored by NIELIT approved Institutes which are permitted to conduct BCC Course and having obtained e-provisional no. - irrespective of any educational qualifications;</li>

<li>2 Direct Applicants -irrespective of any educational qualification;</li>

<strong>Duration: </strong><strong>The total duration of the course is 36 hours,</strong> <strong><strong>consisting of:

i)

Theory

10 hours

ii)

Tutorials

4 hours

iii)

Practicals

22 hours</strong></strong>
<p>Who can conduct the course: The education and training for BCC shall be imparted at the institutions specifically authorised by NIELIT for conducting BCC. In addition, the students can study the course on self-learning mode and can apply as a direct candidate for appearing in the BCC examination conducted by NIELIT.</p> 
