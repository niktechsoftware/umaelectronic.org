<h2> <li> <font color="red">CCC (Course on Computer Concept)</font></h2>
<p>Introduction: This course is designed to aim at imparting a basic level IT Literacy programme for the common man. This programme has essentially been conceived with an idea of giving an opportunity to the common man to attain computer literacy thereby contributing to increased and speedy PC penetration in different walks of life. After completing the course the incumbent should be able to the use the computer for basic purposes of preparing his personnel/business letters, viewing information on internet (the web), receiving and sending mails, preparing his business presentations, preparing small databases etc. This helps the small business communities, housewives, etc. to maintain their small accounts using the computers and enjoy in the world of Information Technology. This course is, therefore, designed to be more practical oriented.</p>

<strong>Eligibility:</strong><p> The candidates can appear in the NIELIT CCC Examination through following three modes and the eligibility criteria for each mode are indicated against each:</p>

<li>2.1	Candidates sponsored by NIELIT approved Institutes permitted to conduct CCC Course - irrespective of any educational qualifications;</li>
<li>2.2	Candidates sponsored by Government recognized Schools/Colleges having obtained an Unique Identity number from NIELIT for conducting CCC - irrespective of any educational qualifications;</li>
<li>2.3	Direct Applicants (without essentially undergoing the Accredited Course or without being sponsored by a Govt. recognised School/College) -irrespective of any educational qualification;</li>
<strong>Duration: The total duration of the course is 80 hours</strong>, consisting of:
<li>i)	Theory	25 hours</li>
<li>ii)	Tutorials	5 hours</li>
<li>iii)	Practicals	50 hours</li>
<strong>The course could ideally be a two weeks intensive course. </strong>

<p>Who can conduct the course: The education and training for CCC shall be imparted at the institutions specifically authorised by NIELIT for conducting CCC. In addition, the students can study the course on self-learning mode and can apply as a direct candidate for appearing in the CCC examination conducted by NIELIT.</p>
