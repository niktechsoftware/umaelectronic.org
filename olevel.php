<h2><font color="red">DOEACC 'O' Level</font></h2>
               
       <p> 'O' level course of DOEACC Scheme is equivalent to a Foundation Level Course in Computer Applications. Students can acquire this qualification by undergoing this course and passing the examination conducted by the NIELIT. After completion of 'O' Level course.</p>
        <h2>Career Opportunities</h2>

<li>Junior Programmer</li>
<li>EDP Assistant</li>
<li>Web Designer</li>
<li>Lab Demonstrator</li>
<li>Various subjects for the programme are:-</li>

<strong>Module Code	Topic</strong>
<li>M1-R4	IT Tools and Business System</li>
<li>M2-R4	Internet Technology and Web Design</li>
<li>M3-R4	Programming and Problem Solving through ‘C’</li>
<li>M4-R4   Elective (one module out of the following three modules to be chosen)</li>
<li>M4. 1-R4	Application of .Net Technology</li>
<li>M4. 2-R4	Introduction to Multimedia</li>
<li>M4. 3-R4	Introduction to ICT Resources</li>
<li>PR	Practical (Based on M1, M2, M3, M4 module syllabus)</li>
<li>PJ	Project</li>
<li>Eligibility:

<li>10+2 or ITI Certificate course (one year).</li>

<strong><li>Practical</li></strong>

<p>The students are required to devote half of the working hours allotted to each module for practical sessions. Practical assignments are worked out for each theory module. The practical examination is based on the syllabi of M1-R4, M2-R4, M3-R4 and M4-R4 modules.
</p>
<strong><li>Project</li></strong>
<p>
The project provides students an additional hands-on experience in solving a real life problem by applying knowledge and skills gained on completion of theory papers in a course at a given level. For the ‘O’ Level Computer Course, marks are not specifically assigned for the project assignment. However, it is compulsory to submit a project report to complete the course.
</p>
<strong><li>Validity of Registration</li></strong>

<p>For completing the ‘O’ Level course, candidates are allowed to appear for a maximum number of ten consecutive examinations, starting from the examination the candidate is eligible to take, as indicated in the letter of allotment of registration number to the candidate.</p>

<strong><li>Re-registration</li></strong>

<p>The validity period of existing registration will be extended to a full term applicable to the respective levels i.e. ten consecutive examinations for O level. For availing this facility the candidate will have to apply for re-registration within 1 year of expiry of the validity period of existing registration. With re-registration the candidate will be granted the benefit of already cleared papers and will be required to clear only the left over papers.</p>

<strong><li>DURATION: </li></strong>
<li>1 Year Part Time</li>
